                            1. ข้อมูลส่วนบุคคลประเภทใดบ้างที่เราเก็บรวบรวม
                            เราอาจมีการเก็บรวบรวมหรือรับข้อมูลประเภทดังต่อไปนี้ซึ่งอาจรวมไปถึงข้อมูลส่วนบุคคลของท่านโดยตรงหรือโดยอ้อมจากท่านหรือแหล่งข้อมูลอื่น
                            ๆ หรือจากบริษัทในเครือของเรา บริษัทย่อย บริษัทอื่น ๆ หรือ พันธมิตรทางธุรกิจ
                            โดยลักษณะของข้อมูลที่เราเก็บรวบรวมนั้นจะขึ้นอยู่กับบริบทของการติดต่อสื่อสารของท่านกับเรา
                            รวมถึงบริการหรือผลิตภัณฑ์ที่ท่านต้องการจากเราและจากบริษัทที่อยู่ในระบบนิเวศของข้อมูล (Data
                            ecosystem) ของกลุ่มเซ็นทรัล
                            <br><br>
                            1.1 ข้อมูลส่วนตัว เช่น คำนำหน้าชื่อ ชื่อเต็ม เพศ อายุ อาชีพ คุณสมบัติ ตำแหน่งงาน
                            ตำแหน่งหรือฐานะ ประเภทธุรกิจ ชื่อบริษัท สัญชาติ ประเทศที่พำนัก วันเกิด สถานภาพทางการสมรส
                            จำนวนสมาชิกในครอบครัวและบุตร อายุของบุตร ข้อมูลบนบัตรที่ออกโดยรัฐบาล (เช่น
                            เลขที่บัตรประจำตัวประชาชน รูปถ่ายบัตรประจำตัวประชาชน ข้อมูลบนบัตรประจำตัวประชาชน
                            เลขควบคุมหลังบัตรประจำตัวประชาชน (Laser ID) เลขประกันสังคม เลขหนังสือเดินทาง
                            เลขประจำตัวผู้เสียภาษีอากร ข้อมูลใบอนุญาตขับขี่รถยนต์ หรือเอกสารระบุตัวตนที่มีลักษณะเดียวกัน
                            เป็นต้น) รายละเอียดการเข้าเมือง เช่น วันที่เดินทางมาถึงและเดินทางออกประเทศ ลายมือชื่อ เสียง
                            เสียงที่บันทึก รูปถ่าย รูปแบบลักษณะใบหน้าเพื่อการจดจำ ภาพจากกล้องวงจรปิด สถานที่ทำงาน
                            วุฒิการศึกษา ข้อมูลประกันภัย ป้ายทะเบียนรถ ทะเบียนบ้าน รายรับภายในครัวเรือน เงินเดือน
                            และรายรับส่วนตัว เป็นต้น
                            <br><br>
                            1.2 ข้อมูลเพื่อการติดต่อ เช่น ที่อยู่ทางไปรษณีย์ ข้อมูลการจัดส่ง ที่อยู่สำหรับส่งใบแจ้งหนี้
                            ที่อยู่ที่เป็นที่พักอาศัย ที่อยู่ของสถานที่ทำงาน ที่อยู่ตามที่ปรากฏบนหน้าบัตรประจำตัวประชาชน
                            หมายเลขโทรศัพท์ หมายเลขโทรสาร ที่อยู่อีเมล ไลน์ไอดี (LINE ID) บัญชีผู้ใช้ Facebook ไอดี
                            Facebook (Facebook ID) ไอดี Google (Google ID) ไอดี Twitter (Twitter ID)
                            และบัญชีผู้ใช้ในเว็บไซต์โซเชียลมีเดียอื่น ๆ รวมไปถึงข้อมูลผู้ติดต่อ เช่น หมายเลขโทรศัพท์
                            ข้อมูลการติดต่อบนช่องทางอื่น ๆ (เช่น การติดต่อสื่อสารเป็นลายลักษณ์อักษรกับท่าน) <br> <br>
                            <br><br>
                            1.3 ข้อมูลการเป็นสมาชิก เช่น รายละเอียดข้อมูลบัญชีสมาชิก หมายเลขบัตรสมาชิก คะแนนสะสม
                            วันที่ออกบัตร/วันหมดอายุ รหัสสมาชิก (เช่น หมายเลขบัตรสมาชิก The 1 รหัส The 1 (The 1 ID)
                            รหัสซีเบล (Siebel ID) รหัสสมาชิก (Member ID) รหัสลูกค้า (Customer ID)) ประเภทสมาชิก
                            ประเภทลูกค้า วันและเดือนที่เข้าร่วม/วันที่สมัคร ระยะเวลาการเป็นสมาชิก บัญชีธนาคาร
                            และข้อมูลการชำระเงิน และการสมัครใช้บริการและผลิตภัณฑ์ (เช่น ใบสมัครสมาชิก ใบสมัครประกันภัย
                            เป็นต้น)
                            <br><br>
                            1.4 ข้อมูลทางการเงิน เช่น ข้อมูลบัตรเครดิต/เดบิตหรือข้อมูลทางธนาคาร หมายเลขบัตรเครดิต/เดบิต
                            ประเภทของบัตรเครดิต วันเดือนปีที่ออก/ วันเดือนปีที่หมดอายุ รอบบิล รายละเอียดบัญชี
                            รายละเอียดเกี่ยวกับบัญชีธนาคาร หมายเลขประจำตัวพร้อมเพย์ รายละเอียดและประวัติการชำระเงิน
                            รวมไปถึงข้อมูลของท่านที่เกี่ยวกับรายละเอียดความเสี่ยงสำหรับพันธมิตรทางธุรกิจ
                            รวมไปถึงการจัดอันดับความน่าเชื่อถือทางเครดิตและความสามารถในการชำระหนี้ (credit rating and
                            solvency) และข้อมูลตามแบบประเมินความเสี่ยง (information in accordance with the declaration
                            of suitability) ความเหมาะสมในการทำธุรกรรม และรายละเอียดอื่นใดทางการเงิน
                            <br><br>
                            1.5 ข้อมูลการทำธุรกรรม เช่น ข้อมูลเกี่ยวกับการชำระเงินไปยังและจากท่าน
                            วันที่และ/หรือเวลาที่ชำระเงิน ยอดชำระเงิน ข้อมูลเกี่ยวกับการขอเงินคืน ยอดเงินคืน คะแนนสะสม
                            วันที่และสถานที่ที่ซื้อ หมายเลขการซื้อหรือคำสั่งซื้อ วันนัดหมายเข้ารับบริการ
                            ที่อยู่/วันที่และเวลาในการรับหรือจัดส่ง ข้อความตอบรับของผู้รับ ข้อความลงท้ายในอีเมลของผู้รับ
                            ข้อมูลการรับประกันสินค้า คำร้องเรียนและข้อเรียกร้อง ข้อมูลการจอง ข้อมูลการเช่า ธุรกรรม
                            ประวัติการทำธุรกรรม สถานที่ สถานะของธุรกรรม ธุรกรรมทางการขายในอดีต สถานะ สถานะการทำธุรกรรม
                            พฤติกรรมการซื้อ และข้อมูลอื่น ๆ ของผลิตภัณฑ์และบริการที่ท่านซื้อ ซึ่งรวมถึงแต่ไม่จำกัดเพียง
                            ข้อมูลใด ๆ ที่เกิดจากการใช้ผลิตภัณฑ์/หรือการบริการผ่านแพลตฟอร์มของเรา เช่น
                            กระเป๋าเงินอิเล็กทรอนิกส์ สินทรัพย์ดิจิทัล ผลิตภัณฑ์การให้เงินกู้ยืม
                            ผลิตภัณฑ์เกี่ยวกับการประกันภัย ผลิตภัณฑ์เกี่ยวกับการบริหารจัดการความมั่งคั่ง เป็นต้น
                            <br><br>
                            1.6 ข้อมูลทางเทคนิค เช่น เลขที่อยู่ไอพีหรืออินเทอร์เน็ตโพรโทคอล (IP address) คุกกี้ ที่อยู่
                            media access control (MAC) เว็บบีคอน (web beacon) ล็อก (Log) รหัสอุปกรณ์ (Device ID) (เช่น
                            International Mobile Equipment Identity (IMEI) Electronic Serial Number (ESN) Mobile
                            Equipment Identifier (MEID) และ Serial Number (SN)) รุ่นอุปกรณ์และประเภทของอุปกรณ์
                            รูปแบบของฮาร์ดแวร์และซอฟต์แวร์ของอุปกรณ์ที่ใช้ เวลาการเปิดใช้งานอุปกรณ์ เครือข่าย
                            ข้อมูลการเชื่อมต่อ ข้อมูลการเข้าถึง ข้อมูลการเข้าใช้งานแบบ single sign-on (SSO)
                            การเข้าสู่ระบบ (Login log) ระยะเวลาและสถานที่ที่เข้าถึง ระยะเวลาที่ใช้บนหน้าเพจของเรา
                            ข้อมูลการเข้าสู่ระบบ จีพีเอส ละติจูด ลองจิจูด และเวลาที่ใช้ในหน้าเว็บแต่ละหน้า
                            ข้อมูลการล็อกอิน แอปพลิเคชันที่ดาวน์โหลดบนอุปกรณ์สื่อสาร ประวัติการค้นหา ข้อมูลการเรียกดู
                            ประเภทและเวอร์ชันของเบราว์เซอร์ การตั้งค่าเขตเวลา (Time zone setting) และสถานที่ตั้ง
                            ประเภทและเวอร์ชันของปลั๊กอินเบราว์เซอร์ ระบบปฏิบัติการและแพลตฟอร์ม และเทคโนโลยีอื่นๆ
                            บนอุปกรณ์ที่ท่านใช้ในการเข้าถึงแพลตฟอร์มของเรา
                            รวมทั้งข้อมูลทางเทคนิคอื่นใดอันเกิดจากการใช้แพลตฟอร์มและระบบของเรา
                            <br><br>
                            1.7 ข้อมูลเกี่ยวกับพฤติกรรม เช่น ข้อมูลเกี่ยวกับพฤติกรรมการซื้อของท่าน
                            และข้อมูลที่ได้รับจากการใช้ผลิตภัณฑ์และบริการของเรา
                            <br><br>
                            1.8 รายละเอียดข้อมูลส่วนตัว เช่น ชื่อผู้ใช้และรหัสผ่านของท่าน
                            รายละเอียดและรูปภาพข้อมูลส่วนตัว การซื้อ ประวัติคำสั่งซื้อ คำสั่งซื้อในอดีต ประวัติการซื้อ
                            สินค้าที่ซื้อ จำนวนสินค้า คำสั่งซื้อหรือคำสั่งเรียกคืนสินค้าโดยท่าน
                            คำสั่งซื้อผ่านทางเว็บไซต์ รายละเอียดการเก็บเงินปลายทาง (Cash on Delivery) ไอดีคำสั่งซื้อ
                            (order ID) บันทึกข้อมูลทางการเงิน เลขรหัสลับส่วนตัว (PIN) ความสนใจของท่าน ความชอบ
                            การตอบรับและผลสำรวจ การสำรวจความพึงพอใจ การใช้โซเชียลมีเดีย ข้อมูลการเข้าร่วม
                            ข้อมูลโปรแกรมการเป็นลูกค้าอย่างต่อเนื่อง (Loyalty programs) รหัสส่วนลดและโปรโมชั่นที่ท่านใช้
                            รายละเอียดคำสั่งซื้อของลูกค้า การบริการลูกค้า การเข้าร่วมนิทรรศการการค้าและกิจกรรม
                            นิทรรศการการค้า การดำเนินคดี การทดสอบและทดลองใช้งาน
                            <br><br>
                            1.9 ข้อมูลการใช้งาน เช่น ข้อมูลเกี่ยวกับการค้นหาหรือใช้งานของท่านบนเว็บไซต์ แพลตฟอร์ม
                            แอปพลิเคชัน การใช้ผลิตภัณฑ์และบริการของเรา สินค้าในรถเข็น รายการสินค้าที่สนใจ
                            บันทึกการแจ้งเตือนเกี่ยวกับสินค้าลดราคา การติดตามร้านค้า (Follow-shop record)
                            เวลาที่คลิกครั้งสุดท้าย (Timestamp of last click) และบันทึกคำถาม-คำตอบ
                            <br><br>
                            1.10 ข้อมูลทางการตลาดและการติดต่อสื่อสาร เช่น
                            ความต้องการของท่านในการรับข้อมูลทางการตลาดจากเรา บริษัทในเครือของเรา บริษัทย่อย บุคคลภายนอก
                            พันธมิตรทางธุรกิจ และรูปแบบการติดต่อสื่อสารที่ต้องการ และ/หรือ
                            <br><br>
                            1.11 ข้อมูลที่ละเอียดอ่อน เช่น เชื้อชาติ ศาสนา ความคิดเห็นทางการเมือง ลายนิ้วมือ
                            ระบบจดจำใบหน้า ข้อมูลอัตลักษณ์บุคคล (Biometrics) ใบหน้า ข้อมูลจากการจดจำม่านตา
                            ข้อมูลสุขภาพหรือสภาพทางร่างกายหรือจิตใจ ข้อมูลทางพันธุกรรม ประวัติทางการแพทย์ ภาวะทุพพลภาพ
                            และประวัติอาชญากรรม
                            <br><br>
                            หากท่านได้ให้ข้อมูลส่วนบุคคลของบุคคลที่สามแก่เรา เช่น ชื่อ นามสกุล ที่อยู่
                            และหมายเลขโทรศัพท์เพื่อติดต่อในกรณีฉุกเฉิน รายได้ของบุคคลในครอบครัว หรือ
                            ในกรณีที่ท่านเข้าใช้บริการในแพลตฟอร์มใด ๆ ของเรา หากท่านให้ความยินยอม
                            เราสามารถเข้าถึงและเก็บรวบรวมข้อมูลส่วนบุคคลของบุคคลที่สามที่เกี่ยวข้องกับท่านได้ เช่น
                            ข้อมูลชื่อ รูปภาพ และ/หรือ หมายเลขโทรศัพท์ ตลอดจนข้อมูลส่วนบุคคลและข้อมูลสำหรับติดต่อของ
                            ครอบครัว เพื่อน บุคคลที่ติดต่อได้ในกรณีฉุกเฉิน บุคคลตามคำแนะนำ
                            หรือบุคคลอ้างอิงที่เข้าถึงได้จากโทรศัพท์มือถือของท่าน เป็นต้น
                            กรุณาแจ้งบุคคลเหล่านั้นให้ทราบถึงนโยบายความเป็นส่วนตัวฉบับนี้
                            และ/หรือขอความยินยอมจากบุคคลเหล่านั้น หากจำเป็น
                            <br><br>
                            เราจะเก็บรวบรวม ใช้
                            หรือเปิดเผยข้อมูลที่ละเอียดอ่อนก็ต่อเมื่อได้รับความยินยอมโดยชัดแจ้งจากท่าน
                            หรือในกรณีที่กฎหมายอนุญาตให้ดำเนินการได้
                            <br><br>
                            เราจะเก็บรวบรวมข้อมูลของผู้เยาว์ คนเสมือนไร้ความสามารถ
                            คนไร้ความสามารถต่อเมื่อได้รับความยินยอมจากผู้ใช้อำนาจปกครองที่มีอำนาจกระทำการแทนผู้เยาว์
                            ผู้พิทักษ์ หรือผู้อนุบาล เท่านั้น
                            เราจะไม่เก็บรวบรวมข้อมูลจากลูกค้าที่เรารู้โดยแจ้งชัดว่าอายุต่ำกว่ายี่สิบปี
                            หรือจากคนเสมือนไร้ความสามารถและคนไร้ความสามารถหากไม่ได้รับความยินยอมจากผู้ใช้อำนาจปกครองที่มีอำนาจกระทำการแทนผู้เยาว์
                            ผู้พิทักษ์ หรือผู้อนุบาล ในกรณีที่จำเป็นต้องได้รับความยินยอม ในกรณีที่เราทราบว่า
                            เราได้เก็บรวบรวมข้อมูลส่วนบุคคลโดยไม่มีเจตนาจากบุคคลใด ๆ ที่อายุต่ำกว่ายี่สิบปี
                            คนเสมือนไร้ความสามารถ
                            หรือคนไร้ความสามารถโดยไม่ได้รับความยินยอมจากผู้ใช้อำนาจปกครองที่มีอำนาจกระทำการแทนผู้เยาว์
                            ผู้พิทักษ์ หรือผู้อนุบาล เราจะดำเนินการลบข้อมูลดังกล่าวทันที
                            หรือประมวลผลเฉพาะในส่วนของข้อมูลที่เราสามารถกระทำได้บนฐานทางกฎหมายอื่น ๆ
                            นอกเหนือจากการขอความยินยอม
                            <br><br>
                            2. เหตุใดเราจึงเก็บรวบรวม ใช้ หรือเปิดเผยข้อมูลส่วนบุคคลของท่าน
                            2.1. วัตถุประสงค์ที่ต้องได้รับความยินยอมจากท่าน
                            การตลาดและการติดต่อสื่อสาร: เราเก็บรวบรวม ใช้
                            และเปิดเผยข้อมูลส่วนบุคคลของท่านเพื่อมอบสิทธิพิเศษและโปรโมชั่น การลดราคา ข้อเสนอพิเศษ
                            การโฆษณา การแจ้งเตือน ข่าวสาร ข้อมูล การตลาดและการติดต่อสื่อสารต่างๆ
                            เกี่ยวกับผลิตภัณฑ์และบริการจากเรา กลุ่มเซ็นทรัล บริษัทในเครือ บริษัทย่อย
                            และ/หรือพันธมิตรทางธุรกิจของเรา ซึ่งเราไม่สามารถอาศัยฐานทางกฎหมายอื่น
                            <br><br>
                            กรุณาติดต่อ ศูนย์บริการลูกค้าออฟฟิศเมท โทร 1281 กด 8
                            เพื่อจัดการความยินยอมของท่านสำหรับการตลาดและการติดต่อสื่อสาร สำหรับสมาชิก The 1 กรุณาติดต่อ
                            ศูนย์บริการลูกค้า เดอะวัน คอลเซ็นเตอร์ โทร 0-2660-1000
                            เพื่อจัดการความยินยอมของท่านสำหรับการตลาดและการติดต่อสื่อสาร
                            <br><br>
                            2.2. วัตถุประสงค์และฐานทางกฎหมายอื่นๆ ที่เราใช้เพื่อการประมวลผลข้อมูลส่วนบุคคลของท่าน
                            เราอาจอาศัย (1) ฐานทางสัญญาในการเข้าทำสัญญาหรือการปฏิบัติตามสัญญากับท่าน (2)
                            หน้าที่ตามกฎหมาย เพื่อการปฏิบัติหน้าที่ตามกฎหมายของเรา (3)
                            ประโยชน์โดยชอบด้วยกฎหมายของเราและของบุคคลภายนอก (4)
                            ประโยชน์ซึ่งจำเป็นเพื่อการป้องกันหรือระงับอันตรายต่อชีวิต ร่างกาย หรือสุขภาพของบุคคล
                            และ/หรือ (5) ประโยชน์สาธารณะ สำหรับการดำเนินภารกิจเพื่อประโยชน์สาธารณะ
                            หรือการใช้สิทธิของเจ้าหน้าที่ของรัฐ
                            <br><br>
                            เราอาจเก็บรวบรวม ใช้ และเปิดเผยข้อมูลส่วนบุคคลของท่านเพื่อวัตถุประสงค์ดังต่อไปนี้
                            <br><br>
                            1. เพื่อจัดหาผลิตภัณฑ์และบริการให้แก่ท่าน
                            เพื่อเข้าทำสัญญาและจัดการความสัมพันธ์ตามสัญญาระหว่างเรากับท่าน
                            เพื่อสนับสนุนและดำเนินกิจกรรมอื่น ๆ ที่เกี่ยวข้องกับบริการ หรือผลิตภัณฑ์ต่าง ๆ ดังกล่าว
                            เพื่อจัดการการจองให้สำเร็จ
                            และเพื่อดำเนินธุรกรรมทางการเงินและบริการที่เกี่ยวข้องกับการชำระเงิน
                            รวมถึงการตรวจสอบและการยืนยันและยกเลิกธุรกรรม เพื่อประมวลผลคำสั่งซื้อของท่าน การจัดส่ง
                            การรับสินค้า และการส่งคืนสินค้า การคืนเงินและการเปลี่ยนสินค้าหรือบริการ
                            เพื่อรายงานสถานะการจัดส่งสินค้าและเพื่อดำเนินกิจกรรมภายในคลังสินค้า รวมถึง การรับ การจัด
                            และการติดฉลากผลิตภัณฑ์ เพื่อตรวจสอบการรับประกัน เพื่อให้บริการหลังการขาย รวมถึง
                            การนัดหมายบริการการบำรุงรักษาและการอำนวยความสะดวก
                            <br><br>
                            2. การตลาดและการติดต่อสื่อสาร เพื่อมอบสิทธิพิเศษและโปรโมชั่น ข่าวสาร การลดราคา ข้อเสนอพิเศษ
                            การโฆษณา การแจ้งเตือน ข่าวสาร ข้อมูล การตลาดและการติดต่อสื่อสารต่างๆ
                            เกี่ยวกับผลิตภัณฑ์และบริการจากเรา กลุ่มเซ็นทรัล บริษัทในเครือ บริษัทย่อย
                            และพันธมิตรทางธุรกิจ
                            <br><br>
                            3. โปรโมชั่น ข้อเสนอพิเศษ โปรแกรมการเป็นลูกค้าอย่างต่อเนื่อง (Loyalty programs)
                            โปรแกรมการสะสมคะแนน (Reward programs) การจับรางวัล การแข่งขัน และข้อเสนอ/โปรโมชั่นอื่น ๆ
                            เพื่อให้ท่านได้เข้าร่วมหรือรับโปรโมชั่น ข้อเสนอพิเศษ โปรแกรมการเป็นลูกค้าอย่างต่อเนื่อง
                            (Loyalty programs) โปรแกรมการสะสมคะแนน การชิงโชค สิทธิพิเศษ การจับรางวัล การแข่งขัน
                            และข้อเสนอ/โปรโมชั่นอื่น ๆ (เช่น การส่งอีเมลแจ้งเตือน
                            การโอนข้อมูลส่วนบุคคลของท่านไปยังคู่ค้า/พันธมิตร) เพื่อการเข้าร่วมกิจกรรม และการจัดอบรม
                            และบริการทั้งหมดที่เกี่ยวข้องกับการโฆษณา โดยรวมถึง
                            การประมวลผลและจัดการการลงทะเบียนบัญชีผู้ใช้งานของท่าน การลงทะเบียนรับของขวัญ
                            การลงทะเบียนเข้าร่วมกิจกรรม เพื่อการประมวลคะแนน การสะสม เพิ่ม แลกเปลี่ยน หรือได้รับคะแนน
                            การแลกหรือจ่ายด้วยคะแนน และการโอนคะแนน
                            เพื่อการตรวจสอบประวัติการใช้งานทั้งทางออนไลน์และออฟไลน์ เพื่อการมอบบัตรกำนัล บัตรของขวัญ
                            และออกใบแจ้งหนี้
                            <br><br>
                            4. การลงทะเบียนและการพิสูจน์ยืนยันตัวตน เพื่อลงทะเบียน ตรวจสอบ พิสูจน์ ยืนยัน ระบุและ/หรือ
                            รับรองท่านหรือตัวตนของท่าน
                            <br><br>
                            5. เพื่อดูแลความสัมพันธ์ระหว่างเรากับท่าน
                            เพื่อติดต่อสื่อสารกับท่านเกี่ยวกับผลิตภัณฑ์และการบริการที่ท่านได้รับจากเรา
                            ทั้งจากภายในระบบนิเวศของข้อมูล (Data ecosystem) ของกลุ่มเซ็นทรัล บริษัทในเครือของเรา
                            บริษัทย่อย และจากพันธมิตรทางธุรกิจของเรา เพื่อจัดการในด้านต่าง ๆ
                            ที่เกี่ยวข้องกับการบริการลูกค้า ไม่ว่าจะเป็น คำถาม คำขอ ผลตอบรับ คำร้องเรียน ข้อเรียกร้อง
                            ข้อพิพาท หรือการเยียวยาชดใช้ค่าเสียหาย เพื่อให้ความช่วยเหลือทางเทคนิคและแก้ไขปัญหาทางเทคนิค
                            เพื่อประมวลผลและปรับปรุงข้อมูลของท่าน
                            เพื่ออำนวยความสะดวกแก่ท่านในการใช้ผลิตภัณฑ์และรับบริการของเรา
                            <br><br>
                            6. การมอบประสบการณ์เฉพาะบุคคล (Personalization)
                            การวิเคราะห์ข้อมูลตามความสนใจหรือพฤติกรรมของลูกค้า (Profiling) และการวิเคราะห์ข้อมูล (Data
                            Analytics) เพื่อแนะนำผลิตภัณฑ์และบริการที่ท่านอาจสนใจ ระบุความชอบของท่าน
                            และมอบประสบการณ์ที่เหมาะสมกับท่าน เพื่อเรียนรู้เพิ่มเติมเกี่ยวกับท่าน
                            ผลิตภัณฑ์และบริการที่ท่านได้รับ และผลิตภัณฑ์และการบริการอื่นๆ ที่ท่านอาจสนใจ
                            เพื่อประเมินความความสนใจหรือพฤติกรรมของท่านที่มีต่อผลิตภัณฑ์และการบริการ
                            เพื่อดำเนินการวิเคราะห์ข้อมูล (Data analytics) การทำความสะอาดข้อมูล (Data cleansing)
                            และการวิเคราะห์ข้อมูลตามความสนใจหรือพฤติกรรมของลูกค้า (Data profiling)
                            เพื่อทำการค้นคว้าวิจัยทางการตลาด ทำแบบสำรวจ ประเมินผล พฤติกรรม สถิติและการแบ่งประเภท
                            รูปแบบและกระแสความนิยมด้านการบริโภค การวิเคราะห์ข้อมูลตามความสนใจหรือพฤติกรรม (Profiling)
                            จากการประมวลผลข้อมูลส่วนบุคคลของท่าน เช่น
                            การประมวลผลจากชนิดของผลิตภัณฑ์และประเภทของการบริการที่ท่านใช้หรือได้รับ
                            ช่องทางที่ท่านต้องการได้รับการติดต่อ เพื่อรู้จักท่านมากขึ้น
                            และเพื่อยกระดับการดำเนินธุรกิจให้ดียิ่งขึ้น
                            ตลอดจนเพื่อปรับเปลี่ยนเนื้อหาให้ตรงกับความพึงพอใจ
                            เพื่อพิจารณาความมีประสิทธิภาพของแคมเปญส่งเสริมการขาย
                            เพื่อระบุและแก้ไขปัญหาที่เกิดขึ้นกับผลิตภัณฑ์และการบริการที่มีอยู่
                            เพื่อการพัฒนาคุณภาพของข้อมูล เพื่อจุดประสงค์ดังกล่าว
                            เราจะเก็บรวบรวมและเปิดเผยข้อมูลส่วนบุคคลของท่านเพื่อความสนใจและผลประโยชน์ของท่าน
                            และเพื่อประโยชน์โดยชอบด้วยกฎหมายและการดำเนินธุรกิจของกลุ่มเซ็นทรัล บริษัทในเครือ
                            บริษัทย่อยและพันธมิตรทางธุรกิจ
                            ซึ่งไม่เกินกว่าสิทธิขั้นพื้นฐานในข้อมูลส่วนบุคคลของเจ้าของข้อมูลส่วนบุคคล ทั้งนี้
                            เราจะขอความยินยอมจากท่านเป็นคราว ๆ ไป หากจำเป็น
