                            7. เพื่อยกระดับการดำเนินธุรกิจ ผลิตภัณฑ์และการบริการให้ดียิ่งขึ้น เพื่อประเมิน พัฒนา
                            จัดการและยกระดับ ค้นคว้าวิจัยและพัฒนาการให้บริการ ผลิตภัณฑ์
                            ระบบและการดำเนินธุรกิจให้แก่ท่านและลูกค้าของเราในระบบนิเวศของข้อมูล (Data ecosystem)
                            ของกลุ่มเซ็นทรัลทั้งหมด และรวมถึงแต่ไม่จำกัดเพียงพันธมิตรทางธุรกิจของเรา
                            เพื่อระบุและแก้ไขปัญหาที่เกิดขึ้น เพื่อจัดทำรายงานภาพรวมของข้อมูลแบบไม่ระบุชื่อ
