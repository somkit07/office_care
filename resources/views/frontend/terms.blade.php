                            หรือผู้รับจ้างในการให้บริการในนามของเรา
                            หรืออำนวยความสะดวกในการมอบผลิตภัณฑ์และบริการให้กับท่าน
                            โดยเราอาจแบ่งปันข้อมูลส่วนบุคคลของท่านไปยังผู้ให้บริการหรือผู้จัดจำหน่ายซึ่งเป็นบุคคลภายนอก
                            ต่อไปนี้ ซึ่งรวมถึงแต่ไม่จำกัดเพียง (1) ผู้พัฒนา/ให้บริการโครงสร้างพื้นฐาน อินเทอร์เน็ต
                            โครงสร้างพื้นฐานทางเทคนิค ซอฟต์แวร์และเว็บไซต์ และผู้ให้บริการด้านเทคโนโลยีสารสนเทศ (2)
                            ผู้ให้บริการโลจิสติกส์และคลังสินค้า (3) ผู้ให้บริการด้านการชำระเงิน (4)
                            บริษัทตัวแทนหรือหน่วยงานวิจัย (5) ผู้ให้บริการด้านการวิเคราะห์ข้อมูล (6)
                            บริษัทตัวแทนหรือหน่วยงานสำรวจข้อมูล (7) ผู้ตรวจสอบบัญชี (8)
                            บริษัทตัวแทนหรือหน่วยงานด้านการติดต่อสื่อสาร สื่อโฆษณาและการตลาด (9) ศูนย์บริการข้อมูลลูกค้า
                            (call center) (10) ผู้จัดงานและกิจกรรม (11) บริษัทตัวแทนการขาย (12)
                            ผู้ให้บริการด้านโทรคมนาคมและการติดต่อสื่อสาร (13) ผู้ให้บริการและตัวแทนด้านการชำระเงิน
                            ระบบการชำระเงิน การยืนยันตัวตน และการตรวจสอบและอ่านข้อมูลด้วยอุปกรณ์อิเล็กทรอนิกส์ (Dip Chip
                            Service) (14) ผู้ให้บริการด้านการบริหารจัดการที่เป็นบุคคลภายนอกที่ได้รับการว่าจ้าง (15)
                            ผู้ให้บริการด้านการจัดเก็บข้อมูลและบริการคลาวด์ (Cloud) (16)
                            ผู้ให้บริการด้านการตรวจสอบและยืนยันข้อมูล (เน็ตเบย์ และกรมการปกครอง) (17) พนักงานเดินเอกสาร
                            (18) ผู้ให้บริการด้านการพิมพ์
                            ในระหว่างการให้บริการต่าง ๆ ดังกล่าว ผู้ให้บริการอาจจะต้องเข้าถึงข้อมูลส่วนบุคคลของท่าน
                            อย่างไรก็ตาม
                            เราจะให้ข้อมูลแก่ผู้ให้บริการของเราเพียงเท่าที่จำเป็นสำหรับการให้บริการดังกล่าวเท่านั้น
                            และจะขอให้ผู้ให้บริการไม่ใช้ข้อมูลของท่านเพื่อวัตถุประสงค์อื่นแต่อย่างใด
                            <br><br>
                            3.3 พันธมิตรทางธุรกิจของเรา
                            เราอาจโอนข้อมูลส่วนบุคคลของท่านไปยังพันธมิตรทางธุรกิจด้านการธนาคาร การเงิน การให้สินเชื่อ
                            การให้กู้ยืม การบริหารสินทรัพย์ การลงทุน การประกันภัย บัตรเครดิต โทรคมนาคม การตลาด
                            การค้าปลีก อี-คอมเมิร์ซ (E-commerce) คลังสินค้าและการขนส่งโลจิสติกส์ ศูนย์สุขภาพ
                            ผลิตภัณฑ์และบริการเพื่อการดำเนินชีวิต สปาและศูนย์ออกกำลังกาย โปรแกรมการสะสมคะแนน (Reward
                            program) และโปรแกรมการเป็นลูกค้าอย่างต่อเนื่อง (Loyalty program) และการวิเคราะห์ข้อมูล (Data
                            analytics) รวมถึง
                            แพลตฟอร์มของผู้ขายหรือผู้ให้บริการซึ่งเราอาจได้ร่วมกันเสนอผลิตภัณฑ์หรือบริการ
                            หรือที่ทางผู้ขายหรือผู้ให้บริการได้เสนอผลิตภัณฑ์หรือบริการให้กับท่าน ทั้งนี้
                            ข้อมูลที่ส่งต่อหรือแบ่งปันกันในลักษณะนี้นั้นจะอยู่ภายใต้นโยบายความเป็นส่วนตัวของบุคคลภายนอก
                            มิใช่ภายใต้นโยบายความเป็นส่วนตัวฉบับนี้
                            <br><br>
                            3.4 เว็บไซต์ต่าง ๆ ในสื่อสังคมออนไลน์
                            ท่านสามารถล็อกอินเข้าสู่เว็บไซต์และแพลตฟอร์มของเราได้โดยไม่ต้องกรอกข้อมูลในแบบฟอร์ม
                            ในกรณีที่ท่านเข้าสู่ระบบผ่านระบบล็อคอินของสื่อสังคมออนไลน์
                            ท่านให้ความยินยอมโดยชัดแจ้งให้เราเข้าถึงและจัดเก็บข้อมูลสาธารณะที่อยู่ในบัญชีต่าง ๆ
                            ในสื่อสังคมออนไลน์ของท่าน (เช่น Facebook Google หรือ Instagram) รวมไปถึงข้อมูลอื่น ๆ
                            ที่ปรากฏขึ้นระหว่างการใช้ระบบล็อกอินผ่านสื่อสังคมออนไลน์ดังกล่าว นอกจากนี้
                            เราอาจส่งอีเมลของท่านไปยังสื่อสังคมออนไลน์
                            เพื่อเป็นการระบุตัวตนว่าท่านเป็นสมาชิกของบรรดาสื่อสังคมออนไลน์ที่เกี่ยวข้องหรือไม่
                            และเพื่อเป็นการลงโฆษณาที่เหมาะสมกับความชอบและที่เกี่ยวข้องบนบัญชีสื่อสังคมออนไลน์ของท่าน
                            ตามความเหมาะสม เรายังร่วมมือกับบุคคลภายนอกอื่น ๆ
                            ซึ่งจะทำให้ท่านสามารถเข้ารับบริการหรือเข้าร่วมรับข้อเสนอโปรโมชั่นของบุคคลภายนอกอื่น ๆ
                            เหล่านั้นได้ ยกตัวอย่างเช่น สำหรับบางบริษัท
                            ท่านสามารถใช้เลขโปรแกรมการเป็นลูกค้าอย่างต่อเนื่อง (Loyalty program number) ของท่าน
                            หรือล็อกอินเข้าสู่ระบบออนไลน์ของท่าน เพื่อรับหรือสมัครบริการของบริษัทนั้น ๆ ได้ นอกจากนี้
                            ท่านสามารถเชื่อมบัญชีสื่อสังคมออนไลน์ของท่านกับบัญชีบริการออนไลน์ของท่านได้
                            หรือเข้าสู่ระบบของบัญชีบริการออนไลน์ของท่านจากบัญชีสื่อสังคมออนไลน์ของท่าน ทั้งนี้
                            เมื่อท่านสมัครบริการต่าง ๆ ดังกล่าวนั้น
                            เราจะแบ่งปันข้อมูลส่วนบุคคลของท่านไปยังบุคคลภายนอกที่ท่านได้ทำการสมัครบริการไว้
                            หากท่านไม่ประสงค์ที่จะแบ่งปันข้อมูลส่วนบุคคลของท่านด้วยวิธีการนี้
                            โปรดอย่าให้เลขโปรแกรมการเป็นลูกค้าอย่างต่อเนื่อง (Loyalty program number)
                            หรือหมายเลขโปรแกรมสะสมคะแนน (Reward program number) ของท่านแก่บุคคลภายนอก
                            และอย่าสมัครรับข้อเสนอโปรโมชั่นโดยใช้บัญชีบริการออนไลน์ของท่าน
                            รวมทั้งอย่าเชื่อมต่อบัญชีบริการออนไลน์ของท่านเข้ากับบัญชีที่ให้บริการโดยบุคคลภายนอก
                            ข้อมูลที่ส่งต่อหรือแบ่งปันกันในลักษณะนี้นั้นจะอยู่ภายใต้นโยบายความเป็นส่วนตัวของบุคคลภายนอก
                            มิใช่ภายใต้นโยบายความเป็นส่วนตัวฉบับนี้
                            <br><br>
                            3.5 บุคคลภายนอกตามที่กฎหมายกำหนด ในบางกรณี
                            เราอาจจะจำเป็นที่จะต้องเปิดเผยหรือแบ่งปันข้อมูลส่วนบุคคลของท่านเพื่อปฏิบัติตามกฎหมาย
                            หรือกฎข้อบังคับ ซึ่งรวมถึง การปฏิบัติตามหน่วยงานที่บังคับใช้กฎหมาย ศาล เจ้าพนักงาน
                            หน่วยงานรัฐ หรือบุคคลภายนอกอื่น ๆ
                            ในกรณีที่เราเชื่อว่าจำเป็นจะต้องปฏิบัติตามหน้าที่ตามกฎหมายหรือกฎข้อบังคับทางกฎหมาย
                            หรือเพื่อการปกป้องสิทธิของเรา สิทธิของบุคคลอื่น หรือเพื่อความปลอดภัยของบุคคล
                            หรือเพื่อตรวจสอบ ป้องกัน หรือจัดการเกี่ยวกับปัญหาการทุจริต หรือด้านความมั่นคงหรือความปลอดภัย
                            <br><br>
                            3.6 ที่ปรึกษาด้านต่าง ๆ ที่ปรึกษาด้านต่าง ๆ นั้นรวมถึงทนายความ
                            ผู้เชี่ยวชาญทางเทคนิคและผู้ตรวจสอบบัญชีที่ช่วยในการประกอบธุรกิจ การดำเนินคดี
                            หรือจัดการเกี่ยวกับข้อเรียกร้องทางกฎหมาย
                            <br><br>
                            3.7 สมาคม และองค์กรต่าง ๆ เราอาจโอนข้อมูลส่วนบุคคลของท่านไปยังสมาคมต่าง ๆ เช่น
                            สมาคมการค้าผู้ให้บริการชำระเงินอิเล็กทรอนิกส์ไทย (TEPA)
                            สำนักงานพัฒนาธุรกรรมทางอิเล็กทรอนิกส์ (สพธอ.) สมาคมสหพันธ์องค์กรผู้บริโภค (สสอบ.)
                            มูลนิธิเพื่อผู้บริโภค หอการค้าไทยและสภาหอการค้าแห่งประเทศไทย
                            สมาคมผู้ประกอบการพาณิชย์อิเล็กทรอนิกส์ไทย สมาคมค้าปลีก สมาคมศูนย์การค้า
                            หรือกลุ่มแยกราชประสงค์
                            <br><br>
                            3.8 ผู้รับโอนสิทธิและ/หรือหน้าที่
                            ผู้รับโอนสิทธิและ/หรือหน้าที่นั้นหมายความรวมถึงบุคคลภายนอกในกรณีมีการปรับโครงสร้างองค์กร
                            การควบรวมกิจการ การโอนกิจการไม่ว่าจะบางส่วน หรือทั้งหมด การขาย การซื้อ การร่วมลงทุน
                            การโอนสิทธิ การโอน หรือการจำหน่ายกิจการ ทรัพย์สิน หรือหุ้น หรือการทำธุรกรรมที่คล้ายกัน
                            ไม่ว่าบางส่วนหรือทั้งหมด
                            ผู้รับโอนสิทธิและ/หรือหน้าที่ของเราจะปฏิบัติตามนโยบายความเป็นส่วนตัวฉบับนี้เพื่อเป็นการเคารพต่อข้อมูลส่วนบุคคลของท่าน
